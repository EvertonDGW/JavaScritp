/* Os módulos ES6 (também conhecidos como ECMAScript 2015 Modules ou JavaScript Modules) são uma maneira padronizada de organizar e compartilhar código JavaScript entre diferentes arquivos e partes de um aplicativo. A introdução dos módulos ES6 trouxe uma maneira nativa e mais robusta de modularizar o código, facilitando a manutenção, reutilização e organização.

Principais Conceitos dos Módulos ES6
Exportação:
Você pode exportar variáveis, funções ou classes de um módulo para torná-los disponíveis para outros módulos. Existem duas formas principais de exportação: nomeada e padrão.

Importação:
Você pode importar variáveis, funções ou classes de outros módulos para usá-los no seu próprio código. */